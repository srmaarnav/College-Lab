/* program to find size of various data type using sizeof() */

#include<stdio.h>
int main()

{
	int x;
	printf ("%d byte \n", sizeof(x));
	printf ("%d byte \n", sizeof(int));
	printf ("%d byte \n", sizeof(float));
	printf ("%d byte \n", sizeof(char));
	printf ("%d byte \n", sizeof(double));
}
